import numpy as np

# Create a 1D numpy array
a = np.array([1, 2, 3, 4, 5])

# Print the array
print(a)

# Print the type of the array
print(type(a))

# Print the shape of the array
print(a.shape)

# Print the first element of the array
print(a[0])

# Print the last element of the array
print(a[-1])

# Print the first 3 elements of the array
print(a[:3])

# Print the last 2 elements of the array
print(a[-2:])

# Print the sum of the array
print(a.sum())

# Print the mean of the array
print(a.mean())

# Print the standard deviation of the array
print(a.std())

# Print the maximum value of the array
print(a.max())

# Print the minimum value of the array
print(a.min())

# Print the index of the maximum value of the array
print(a.argmax())

# Print the index of the minimum value of the array
print(a.argmin())

